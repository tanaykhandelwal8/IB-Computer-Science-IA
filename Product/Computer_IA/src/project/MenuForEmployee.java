    private void MenuTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_MenuTableMouseClicked
        // TODO add your handling code here:
        try {
            int row=MenuTable.getSelectedRow();
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:8889/COMP IA","root","root");
            String SelectedRowOption=(MenuTable.getModel().getValueAt(row,0).toString());
            String sql="SELECT * from Menu where Serial="+SelectedRowOption;
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()) {
                serial.setText(rs.getString("Serial"));
                name.setText(rs.getString("Name"));
                price.setText(rs.getString("Price"));
                quantity.setText(rs.getString("Quantity"));
            }
            con.close();
        }
        catch(ClassNotFoundException | SQLException obj) {
            JOptionPane.showMessageDialog(null,obj);
        }
    }//GEN-LAST:event_MenuTableMouseClicked

    private void filterKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_filterKeyReleased
        // TODO add your handling code here:
        try {
            String selection=(String)select.getSelectedItem();
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:8889/COMP IA","root","root");
            String sql="SELECT * from Menu where "+selection+"=?";
            pst=con.prepareStatement(sql);
            pst.setString(1, filter.getText());
            rs=pst.executeQuery();
            MenuTable.setModel(DbUtils.resultSetToTableModel(rs));
        }
        catch(ClassNotFoundException | SQLException obj) {
            JOptionPane.showMessageDialog(null, obj);
        }
    }//GEN-LAST:event_filterKeyReleased

