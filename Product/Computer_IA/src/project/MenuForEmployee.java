    private void receiptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_receiptActionPerformed
        // TODO add your handling code here:
        menuprint.setText("");
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=getConnection();
            menuprint.append("Menu for Dulcedines\nName\t\t\tPrice\t\tQuantity\n");
            pst=con.prepareStatement("SELECT * from Menu");
            rs=pst.executeQuery();
            int i=1;
            while(rs.next()) {
                menuprint.append(i++ + ". "+rs.getString("Name")+" \t\t"+rs.getString("Price")+"\t\t"+rs.getString("Quantity")+"\n");
            }
        }
        catch(ClassNotFoundException | SQLException obj) {
            JOptionPane.showMessageDialog(null, obj);
        }
    }//GEN-LAST:event_receiptActionPerformed

