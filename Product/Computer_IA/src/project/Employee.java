    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(name.getText().toString().equalsIgnoreCase("employee name") || username.getText().toString().equalsIgnoreCase("employee name") || password.getText().toString().equalsIgnoreCase("password")) {
            JOptionPane.showMessageDialog(null, "One or more fields empty");
        }  
        else {
        boolean flag=true;
            while(flag) {
             try {
            Class.forName("com.mysql.cj.jdbc.Driver");
             con=DriverManager.getConnection("jdbc:mysql://localhost:8889/COMP IA","root","root");
            String sql="SELECT Name from Employees WHERE Name='"+name.getText()+"'";
            Statement stat=con.createStatement();
            rs=stat.executeQuery(sql);
            if(rs.next()) {
                JOptionPane.showMessageDialog(null, "User already registered!");
                break;
            }
            
            else {
            sql="INSERT INTO Employees(Name, Username, Password, Status) VALUES (?,?,?,?)";
            con=DriverManager.getConnection("jdbc:mysql://localhost:8889/COMP IA","root","root");
            pst=con.prepareStatement(sql);
            pst.setString(1, name.getText());
            pst.setString(2, username.getText());
            pst.setString(3, password.getText());
            pst.setString(4, Status.getSelectedItem().toString());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Inserted successfully");
            flag=false;
            }
            con.close();
            
        } catch (Exception obj) {
            JOptionPane.showMessageDialog(null,obj);
        }
        showTableData();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

