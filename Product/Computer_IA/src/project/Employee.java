    private void EmployeeTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EmployeeTableMouseClicked
        // TODO add your handling code here:
          try {
            DefaultTableModel model=(DefaultTableModel) EmployeeTable.getModel();
            int row=EmployeeTable.getSelectedRow();
          
            Class.forName("com.mysql.cj.jdbc.Driver");
            con=DriverManager.getConnection("jdbc:mysql://localhost:8889/COMP IA","root","root");          
            String SelectedRowOption=(EmployeeTable.getModel().getValueAt(row,0).toString());
            String sql="SELECT * from Employees where Serial="+SelectedRowOption;
            pst=con.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()) {
                srno.setText(rs.getString("Serial"));
                name.setText(rs.getString("Name"));
                username.setText(rs.getString("Username"));
                password.setText(rs.getString("Password"));
                Status.setSelectedItem(rs.getString("Status"));
            }
            con.close();
        }
        catch(Exception obj) {
            JOptionPane.showMessageDialog(null,obj);
        }
    }//GEN-LAST:event_EmployeeTableMouseClicked

